Third time around. The shareholders who saved Edinburgh Worldwide twice need to show up once more. Vote today. The deadline is today.

─── EXAMPLE 2 ───
Type: PAIRED
Topic: sector (Saba's UKIT ETF)
Trigger: The irony of a hedge fund profiting from a discount problem its own campaigns helped widen.

COMPANY POST:
Boaz Weinstein has spent two years requisitioning investment trust boards. In March, he listed an ETF on the London Stock Exchange that buys shares in investment trusts trading at a discount. The ticker is UKIT. The strategy is to profit from the gap between share price and NAV, the same gap that Saba's campaigns have, in part, widened. There is a certain logic to it. There is also a certain irony. The discount problem is real. Tens of thousands of retail shareholders hold investment trust shares below NAV and would rather not. The question is whether the right response is a New York hedge fund arbitraging the situation, or boards getting better at talking to the people whose votes actually matter. Steppingstone and Curation are working on the second of those.

PERSONAL RESHARE COMMENTARY:
I have some sympathy for the creativity. Rather less for the logic. Buying shares in distressed trusts and calling it a solution is not the same as fixing the underlying problem.

─── EXAMPLE 3 ───
Type: PAIRED
Topic: deal (Waymap at the Royal Hospital for Children and Young People)
Trigger: First children's hospital in the world to deploy Waymap.

COMPANY POST:
Tom Pey founded Waymap after losing his sight at 39. The app guides people through buildings without GPS, WiFi, or any external signal, step by step, accurate to a single metre. In February it went live at the Royal Hospital for Children and Young People in Edinburgh. The first children's hospital in the world to use this kind of personal indoor navigation. Parents arriving at night, unfamiliar with the building, managing anxious children, sometimes without English as their first language. The app takes them from the front door to the exact department. It already runs at Lord's Cricket Ground, across Washington DC's entire Metro system, with Singapore Transport Authority, at the UN. Each one a place where finding your way to exactly the right room was harder than it needed to be. Steppingstone introduced Waymap to Lord's. If your organisation runs a venue, transport network, or large building where this matters, we are glad to make the introduction.

PERSONAL RESHARE COMMENTARY:
Tom lost his sight at 39 and built something that is now guiding families through a children's hospital. That one feels good.

─── EXAMPLE 4 ───
Type: RESHARE (longer-form, 4 short paragraphs)
Topic: sector (FCA + retail shareholders — to be attached to an AIC or Jonathan Simpson-Dent post when relevant)

COMMENTARY:
Retail shareholders in investment trusts are not passive. At Edinburgh Worldwide they have rejected Saba's proposals by over 90% every time they have had a clean vote.

The problem is the rules. A single large shareholder can bring the same proposals to the same board, repeatedly, even after shareholders have already rejected them. The costs are real. The legal bills are real. Director time is finite.

The FCA consultation matters. It will not come in time for Edinburgh Worldwide. But it might come in time for the next one.

In the meantime, boards that talk regularly and directly to retail shareholders are in a better position than those that do not. Edinburgh Worldwide just proved it for the third time.


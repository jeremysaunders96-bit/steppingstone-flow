Edinburgh Worldwide's AGM is tomorrow, 30 April. Third time Saba has come for the board. The first two votes went the right way. This one is closer. On 10 April, two institutions voted with Saba, not because they backed his vision for the trust, but for reasons of their own. That 53.8% to 46.2% result has changed the maths going into tomorrow. The board's offer is straightforward: vote them back in, receive a 50% tender at NAV, then a second full exit once SpaceX IPOs. Jonathan Simpson-Dent has spent months trying to get to that position. It is a fair deal. Saba's alternative, Baillie Gifford out, mandate changed, three New York nominees running a 130-year-old FTSE 250 trust, is not. ISS, Glass Lewis and PIRC have all told shareholders to back the board. If you hold shares in Edinburgh Worldwide, your deadline is today. Steppingstone works with Curation to help investment trust boards talk to the retail shareholders who, as tomorrow will prove again, are the ones who actually decide these things.

